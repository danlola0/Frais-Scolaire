    private void afficherCaissier() {
        String query = "SELECT * FROM caissiers";
        Rs = mycon.getResultat(query);
        dt.setRowCount(0);
        try{
            while(Rs.next()){
                Matricule = Rs.getString("Matricule_caissier");
                Nom = Rs.getString("NOM_CAISSIER");
                Postnom = Rs.getString("POSTNOM_CAISSIER");
                Prenom = Rs.getString("PRENOM_CAISSIER");
                Sexe = Rs.getString("SEXE");
                pass = Rs.getString("PASS");
                
                Object[] Table = {Matricule,Nom,Postnom,Prenom,Sexe,pass};
                dt.addRow(Table);
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Erreur d'affichage\n"+ex.getMessage());
        }
    }
    
