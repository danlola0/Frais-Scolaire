    private void dtcaissierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dtcaissierMouseClicked
        DefaultTableModel dl = (DefaultTableModel)dtcaissier.getModel();
        int index = dtcaissier.getSelectedRow();
        
        tMatricule.setEditable(false);
        tMatricule.setText(dl.getValueAt(index, 0).toString());
        tNom.setText(dl.getValueAt(index, 1).toString());
        tPostnom.setText(dl.getValueAt(index, 2).toString());
        tPrenom.setText(dl.getValueAt(index, 3).toString());
        cSexe.setSelectedItem(dl.getValueAt(index, 4).toString());
        tPass.setText(dl.getValueAt(index, 5).toString());
        
        jButton1.setEnabled(false);
        jButton2.setEnabled(true);
        jButton3.setEnabled(true);
    }//GEN-LAST:event_dtcaissierMouseClicked

