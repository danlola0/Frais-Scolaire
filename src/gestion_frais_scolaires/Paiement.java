    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        Connexion_mysql mycon = new Connexion_mysql();
    
        ResultSet Rs;
    
        String Sexe = null, Classe = null;
        
        String query = "SELECT * FROM eleves WHERE Matricule_Eleve='" + lMatricule.getText() + "'";
        Rs = mycon.getResultat(query);
        query = "SELECT * FROM caissiers WHERE Matricule_caissier='" + tMatcaissier.getText() + "'";
        try{
            while(Rs.next()){
                matEleve = Rs.getString("Matricule_Eleve");
                NomEleve = Rs.getString("NOM")+ " " + Rs.getString("POSTNOM") + " " + Rs.getString("PRENOM");
                Sexe = Rs.getString("SEXE");
                Classe = Rs.getString("CLASSE");
            }
            Rs = mycon.getResultat(query);
            while(Rs.next()){
                matCaissier = Rs.getString("Matricule_caissier");
                NomCaissier = Rs.getString("NOM_CAISSIER") + " " + Rs.getString("POSTNOM_CAISSIER") + " " + Rs.getString("PRENOM_CAISSIER");
            }
            
            Num_recu = tNum_recu.getText();
            Libelle = tLibelle.getText();
            Montant = tMontant.getText();
            Annee_acad = tAnnee.getText();
            Devise = cDevise.getSelectedItem().toString();
            Date_paiement = today;
                    
            new Recu(Num_recu, matEleve, NomEleve, Montant, Libelle, Date_paiement, Classe, Devise, Annee_acad, matCaissier, NomCaissier, Sexe).show();
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Erreur de selection\n"+ex.getMessage());
        }
    }//GEN-LAST:event_jButton7ActionPerformed

