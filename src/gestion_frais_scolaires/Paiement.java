    private void afficherPaiement() {
        
        String query = "SELECT Num_recu,eleve_Mat,caissier_Mat,NOM,NOM_CAISSIER,Libelle,Montant,Annee_acad,Devise,Date_paiement " +
                       "FROM eleves,caissiers,recus " +
                       "WHERE recus.eleve_Mat=eleves.Matricule_Eleve AND recus.caissier_Mat=caissiers.Matricule_caissier";
        
        Rs = mycon.getResultat(query);
        dt.setRowCount(0);
        rowsC = 1;
        matEleves.clear();
        matCaissiers.clear();
        try{
            while(Rs.next()){
                Num_recu = Rs.getString("Num_recu");
                NomEleve = Rs.getString("NOM");
                NomCaissier = Rs.getString("NOM_CAISSIER");
                Libelle = Rs.getString("Libelle");
                Montant = Rs.getString("Montant");
                Annee_acad = Rs.getString("Annee_acad");
                Devise = Rs.getString("Devise");
                Date_paiement = Rs.getString("Date_paiement");
                
                matCaissier = Rs.getString("caissier_Mat");
                matEleve = Rs.getString("eleve_Mat");
                
                matEleves.put(NomEleve, matEleve);
                matCaissiers.put(NomCaissier, matCaissier);
                
                rowsC++;
                
                Object[] Table = {Num_recu,NomEleve,NomCaissier,Libelle,Montant,Annee_acad, Devise, Date_paiement};
                dt.addRow(Table);
            }
            tNum_recu.setText(String.valueOf(rowsC));
            System.out.println(matEleves + " " + matCaissiers);
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Erreur d'affichage\n"+ex.getMessage());
        }
    }
    
